  <h2>Iskustvo vožnje kombinovano sa efikasnošću</h2>
  <iframe width="100%" height="100%" src="https://www.youtube.com/embed/QcpzYWMzuh8" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
  <p>Audi A8 je djelimično Mild-Hybrid. Kombinacija motora sa unutrašnjim sagorijevanjem, vodom hlađenog 48-V starter generatora sa kaišnim prenosom i efikasnog litijum-jonskog akumulatora prvi put postaje dostupna u sklopu serijske opreme. Ova nova jedinica čini osnovu za čitav niz poboljšanja koja su prije svega zaslužna za veću efikasnost i veći komfor u vožnji.</p>
  <p>Starter generator sa kaišnim prijenosom ima dvostruku ulogu: radi kao elektromotor ili kao generator. U okviru svoje prve funkcije na raspolaganje stavlja značajan pogonski moment od 60 Nm. On se u modelu Audi A8 koristi za jedinstvenu funkciju: kada dostignete ciljanu brzinu, u području između 30 i 160 km/h isključuje se sagorijevanje u trajanju od maksimalno 40 sekundi - za komfornu i mirnu vožnju u praznom hodu bez emisija. Starter generator sa kaišnim prijenosom ima dovoljno snage da u svakom trenutku ponovno pokrene motor. Kada dodirnete papučicu gasa, motor će se odmah ponovno pokrenuti.</p>
  <p>Generator ima prednosti i tokom usporavanja: zajedno sa efikasnim akumulatorom iznad zadnje osovine omogućava intenzivniju rekuperaciju. Tako se već potrošena energija vraća u vozilo. Ovaj povrat energije često je efikasniji od pomenute vožnje u praznom hodu - zato menadžment pogona modela A8 konstantno nadzire i analizira situaciju. U tu svrhu podaci sa prednje kamere i  eventualno informacije prediktivne podrške za efikasnu vožnju kombinuju se sa aktuelnim podacima o dionici puta i vrijednostima iz kompleta senzora.</p>
